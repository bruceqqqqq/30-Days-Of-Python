# Day 1 - 30DaysOfPyThon Challenge

print(2 + 5)  # Soma
print(3 - 1)  # Substração
print(2 * 3)  # Multiplicação
print(3 / 2)  # divisão completa
print(3 ** 2)  # potência
print(7 % 3)  # resto da divisão
print(3 // 2) # divisão sem o resto

# Verificando 'type' dos itens

print(type(10))  # Inteiro
print(type(10.5))  # Float
print(type(True))  # Booleano
print(type(1 + 3j))  # Complex == "Numero Imaginario com Numero Inteiro"
print(type('Programming like a boss'))  # String
print(type([1, 2, 3]))  # Lista
print(type((1, 2, 3)))  # Tupla
print(type({'One': 1, 'Two': 2, 'Three': 3})) # Dicionario
print(type({1, 3, 2})) # Set = "Conjunto de unidades unicas"
